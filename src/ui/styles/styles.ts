export function propsShadowByKey(props: any, key: string) {
    return props.theme.shadows[props.theme.level[key] || 0]
}

export function themeColor(theme: Theme, color: ThemeColor) {
    const themeType = theme.palatte.type === 'light' ? 'light' : 'dark'
    return color[themeType]
}

